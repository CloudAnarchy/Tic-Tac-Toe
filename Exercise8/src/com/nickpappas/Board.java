    // Koitaei ka8e pi8anh periptwsh pou uparxei gia na uparxei nikh
    public boolean didGameEnd(){

        if(array[0][0] == array[0][1] && array[0][0] == array[0][2]) return checkWinner(0,0);
        if(array[0][1] == array[1][1] && array[0][1] == array[2][1]) return checkWinner(0,1);
        if(array[0][2] == array[1][2] && array[0][2] == array[2][2]) return checkWinner(0,2);
        if(array[0][0] == array[1][0] && array[0][0] == array[2][0]) return checkWinner(0,0);
        if(array[1][0] == array[1][1] && array[1][0] == array[1][2]) return checkWinner(1,0);
        if(array[2][0] == array[2][1] && array[2][0] == array[2][2]) return checkWinner(2,0);
        if(array[0][0] == array[1][1] && array[0][0] == array[2][2]) return checkWinner(0,0);
        if(array[0][2] == array[1][1] && array[0][2] == array[2][0]) return checkWinner(0,2);
        if(didDraw()) return true;
        return false;
    }

    // Me bash tou symbol blepei poios htan o nikhths
    private boolean checkWinner(int x, int y){

        if(array[x][y] != ' '){

            if(array[x][y] == 'X') this.whoWon = 1;
            if(array[x][y] == 'O') this.whoWon = 2;
            return true;
        }
        return false;
    }

